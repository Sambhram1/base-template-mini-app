export function useMintNFT() {
  const { writeContract, data: hash, error, isPending } = useWriteContract();
  
  const { isLoading: isConfirming, isSuccess: isConfirmed } =
    useWaitForTransactionReceipt({
      hash,
    });

  return {
    mintNFT: (to: Address, productId: string, metadataURI: string, signature: `0x${string}`) =>
      writeContract({
        address: LUXLINK_NFT_ADDRESS,
        abi: LUXLINK_NFT_ABI,
        functionName: 'mintProductNFT',
        args: [to, productId, metadataURI, signature],
      }),
    hash,
    error,
    isPending,
    isConfirming,
    isConfirmed,
  };
}

