// Custom hooks for contract interactions
export function useVerifyNFT(tokenId?: string, chainId?: number) {
  console.log("useVerifyNFT called with:", { tokenId, chainId });
  
  // Validate tokenId to ensure it's a valid numeric string
  const isValidTokenId = tokenId && 
    tokenId.trim() !== '' && 
    tokenId !== '0' && 
    /^\d+$/.test(tokenId.trim()) && // Only contains digits
    !tokenId.includes('http'); // Not a URL
  
  const result = useReadContract({
    address: LUXLINK_NFT_ADDRESS,
    abi: LUXLINK_NFT_ABI,
    functionName: 'verifyAuthenticity',
    args: isValidTokenId ? [BigInt(tokenId.trim())] : undefined,
    query: {
      enabled: !!isValidTokenId,
    },
  });
  
  console.log("useVerifyNFT result:", result);
  return result;
}

